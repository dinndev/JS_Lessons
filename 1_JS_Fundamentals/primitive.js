// Primitive Types
// String
const name = 'John Doe';
console.log(name, typeof name);
// Number
const age = 45;
// Boolean true/false
const hasKids = true;
// Null
const car = null;
console.log(car);
// Undefined
let test;
console.log(test);
// Symbol
const sym = Symbol();
console.log(sym);

// Reference Type - Objects
// Array
const hobbies = ['movies', 'music'];
console.log(hobbies[2]);
// Object literal
const address = {
  city: 'Manila',
  street: 'Bagong Ilog'
}
console.log(address);
// Date
const today = new Date();
console.log(today);
console.log(typeof today);